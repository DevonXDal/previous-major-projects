
/**
 * Enumeration class PlayerClass - Holds the possible classes for the player
 *
 * @author Devon X. Dalrymple
 * @version 2019.11.29-01
 */
public enum PlayerClass
{
    WARRIOR /* Brute, melee focused */, 
    ROGUE /* Fast, well-rounded class with the ability to use more actions then any other */,
    MAGE /* Ranger, uses a limited source of energy to handle conflicts */, 
    PEASANT /* Improves at a higher rate, goes from being a bad to an amazing class as it levels up */, 
}

